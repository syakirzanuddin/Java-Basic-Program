import java.util.Scanner;

public class While {
    public static void main(String args[]) {
      System.out.print("\nprint number 0 until 9: ");
      int i = 0; 
      
      while(i <= 9)
      {
          System.out.print(i);
          
          i++;
      }
      
      System.out.print("\nprint number 1 until 9: ");
      int j = 1;
      
      while(j <= 9)
      {
          System.out.print(j); 
          
          j++;
      }
}

}